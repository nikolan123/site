function shuffleArray(array) {
    const shuffled = [...array];
    for (let i = shuffled.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
    }
    return shuffled;
}

function initializeGames() {
    const gamesArray = Object.values(games);
    const shuffledGames = shuffleArray(gamesArray);
    
    const gamesList = document.getElementById('games-list');
    if (!gamesList) return;
    
    shuffledGames.forEach(game => {
        const gameLink = document.createElement('a');
        gameLink.className = 'game';
        gameLink.href = game.store;
        gameLink.innerHTML = `
            <img src="${game.icon}" width="16" alt="${game.name}">
            <p>${game.name}</p>
        `;
        gamesList.appendChild(gameLink);
    });
}

document.addEventListener('DOMContentLoaded', initializeGames);
